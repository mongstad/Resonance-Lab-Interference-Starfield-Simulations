def _flatten_meta(meta):
    """Ensure JSON-serializable (convert numpy types)."""
    def conv(v):
        import numpy as _np
        if isinstance(v, (list, tuple)):
            return [conv(x) for x in v]
        if hasattr(v, "item"):
            try:
                return v.item()
            except Exception:
                pass
        if isinstance(v, _np.generic):
            return v.tolist()
        return v
    return {str(k): conv(v) for k, v in (meta or {}).items()}

def add_png_metadata(out_path: str, meta: dict):
    """Embed metadata dict into a PNG's tEXt chunk (alongside saving a JSON sidecar)."""
    if not os.path.isfile(out_path):
        return False
    meta = _flatten_meta(meta)
    # Always write sidecar JSON
    try:
        with open(os.path.splitext(out_path)[0] + ".json", "w", encoding="utf-8") as jf:
            json.dump(meta, jf, ensure_ascii=False, indent=2)
    except Exception:
        pass
    if not _HAS_PIL:
        return False
    try:
        im = Image.open(out_path)
        pnginfo = PngImagePlugin.PngInfo()
        # Store a compact JSON under key "params"
        compact = json.dumps(meta, separators=(",", ":"))[:65500]  # keep within sane chunk size
        pnginfo.add_text("params", compact)
        pnginfo.add_text("generated_by", "Resonance Lab")
        pnginfo.add_text("generated_at", datetime.utcnow().isoformat()+"Z")
        im.save(out_path, "PNG", pnginfo=pnginfo)
        return True
    except Exception:
        return False

