# ====== Starfield utilities (interference → sparse stars) ======
def _apply_envelope(X, Y, Z, band=None):
    """Optionally apply a galactic band envelope to densify stars along a stripe.
    band: None or dict(angle_deg, width, strength, y0)
    """
    if not band:
        return Z
    ang = float(band.get("angle_deg", 0.0)) * np.pi/180.0
    w   = float(band.get("width", 0.6))
    k   = float(band.get("strength", 1.8))
    y0  = float(band.get("y0", 0.0))
    # Rotate coords by -ang so band aligns with X-axis
    Xr =  X*np.cos(-ang) - Y*np.sin(-ang)
    Yr =  X*np.sin(-ang) + Y*np.cos(-ang)
    # Gaussian stripe along Yr≈y0
    env = np.exp(-0.5 * ((Yr - y0)/max(1e-6, w))**2)
    return Z * (1.0 + k*env)

def generate_star_mask_from_field(
    X, Y, Z,
    power: float = 6.0,
    percentile: float = 99.7,
    max_stars: int = 8000,
    band=None,
    rng: np.random.RandomState = None
):
    """Turn a continuous interference field into a sparse 'star' mask.
    Steps:
    - rectify and emphasize peaks via power-law
    - optional galactic band envelope
    - threshold by high percentile
    - randomly subsample to max_stars
    Returns: indices (iy, ix) of chosen star pixels and an intensity array for each star.
    """
    if rng is None:
        rng = np.random.RandomState(None)
    # normalize then rectify
    Zn = normalize_field(Z)
    Zp = np.clip(Zn - np.percentile(Zn, 50), 0, 1)  # remove median-ish background
    Zp = Zp**power
    # envelope (densify along a band)
    Zp = _apply_envelope(X, Y, Zp, band=band)
    # threshold at high percentile
    th = np.percentile(Zp, percentile)
    mask = Zp >= th
    ys, xs = np.nonzero(mask)
    if ys.size == 0:
        return np.array([], dtype=int), np.array([], dtype=int), np.array([])
    # sample down to max_stars with weight ~ Zp
    weights = Zp[ys, xs].astype(float)
    if ys.size > max_stars:
        probs = weights / (weights.sum() + 1e-12)
        idx = rng.choice(np.arange(ys.size), size=max_stars, replace=False, p=probs)
        ys, xs, weights = ys[idx], xs[idx], weights[idx]
    return ys, xs, weights

def _stamp_gaussian(canvas, y, x, sigma=0.9, amp=1.0):
    """Add a small Gaussian PSF patch centered at integer pixel (y, x)."""
    h, w = canvas.shape[:2]
    r = max(1, int(3*sigma))
    y0, y1 = max(0, y-r), min(h, y+r+1)
    x0, x1 = max(0, x-r), min(w, x+r+1)
    if y0 >= y1 or x0 >= x1:
        return
    yy, xx = np.mgrid[y0:y1, x0:x1]
    g = np.exp(-((yy - y)**2 + (xx - x)**2)/(2.0*sigma*sigma)) * amp
    canvas[y0:y1, x0:x1] += g

def render_starfield(
    X, Y, ys, xs, intensities,
    out_path: str,
    cmap_mode: str = "mono",  # mono | blackbody
    psf_sigma: float = 0.9,
    gamma: float = 0.8,
    bg: tuple = (0,0,0),
    dpi: int = 180
):
    """Rasterize stars (ys, xs) into an RGB image and save."""
    H, W = X.shape
    # RGB canvas
    canvas = np.zeros((H, W, 3), dtype=np.float32)
    # Normalize intensities to [0,1]
    if intensities.size:
        intens = intensities / (intensities.max() + 1e-12)
    else:
        intens = intensities
    # Color mapping
    def colorize(a):
        if cmap_mode == "blackbody":
            # Simple blue→white→yellow mapping by temp proxy a
            # a≈1 → bluish-white, a≈0 → warm
            r = np.clip(0.8*a + 0.3, 0, 1)
            g = np.clip(0.85*a + 0.2, 0, 1)
            b = np.clip(1.0*a + 0.1, 0, 1)
            return r, g, b
        # mono
        return a, a, a
    # Stamp PSF spots
    for (y, x, a) in zip(ys, xs, intens):
        r, g, b = colorize(a)
        _stamp_gaussian(canvas[...,0], int(y), int(x), sigma=psf_sigma, amp=r)
        _stamp_gaussian(canvas[...,1], int(y), int(x), sigma=psf_sigma, amp=g)
        _stamp_gaussian(canvas[...,2], int(y), int(x), sigma=psf_sigma, amp=b)
    # Tone curve
    canvas = np.clip(canvas, 0, 1) ** gamma
    # Plot via imshow (keeps WCS-like extent if desired)
    fig, ax = plt.subplots(figsize=(8,8))
    ax.set_aspect("equal"); ax.axis("off")
    ax.imshow(canvas, extent=(X.min(), X.max(), Y.min(), Y.max()), origin="lower", interpolation="bilinear")
    fig.tight_layout(pad=0)
    fig.savefig(out_path, dpi=dpi, bbox_inches="tight", pad_inches=0)
    plt.close(fig)

def generate_starfield_components(
    size: int = 1200,
    extent: float = 6.0,
    modes: int = 64,
    mode_types: List[str] = None,
    kx_range=(6.0, 24.0),
    ky_range=(6.0, 24.0),
    kr_range=(4.0, 16.0),
    ktheta_range=(4.0, 16.0),
    amp_range=(0.6, 1.2),
    phase_range=(0.0, 2*np.pi),
    center=(0.0, 0.0),
    seed: str = "random",
    # star shaping
    power: float = 6.0,
    percentile: float = 99.7,
    max_stars: int = 8000,
    psf_sigma: float = 0.9,
    band=None,
    cmap_mode: str = "mono",
    gamma: float = 0.8
):
    """
    Generate the underlying interference field and associated sparse star positions.

    This helper creates a random interference field using ``generate_field`` with
    the supplied ranges, detects bright peaks via ``generate_star_mask_from_field`` and
    returns the coordinate grids and arrays of peak indices and weights.  It is
    primarily used internally by :func:`render_starfield` to rasterize the
    starfield into an image.

    Returns ``(X, Y, ys, xs, weights)`` where ``ys`` and ``xs`` are index
    arrays into the field and ``weights`` encode relative brightness.  To
    convert these into an image use :func:`render_starfield`.
    """
    rng = rng_from_seed(seed)
    X, Y, Z = generate_field(
        size=size, extent=extent, modes=modes,
        mode_types=(mode_types or ["plane", "radial", "spiral"]),
        kx_range=kx_range, ky_range=ky_range, kr_range=kr_range, ktheta_range=ktheta_range,
        amp_range=amp_range, phase_range=phase_range,
        center=center, rng=rng
    )
    ys, xs, weights = generate_star_mask_from_field(
        X, Y, Z,
        power=power,
        percentile=percentile,
        max_stars=max_stars,
        band=band,
        rng=rng,
    )
    return X, Y, ys, xs, weights

