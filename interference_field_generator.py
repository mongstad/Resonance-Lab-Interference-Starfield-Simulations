# ---------------------------- Helpers ----------------------------
def parse_range(two_vals: List[float], name: str) -> Tuple[float, float]:
    if len(two_vals) != 2:
        raise ValueError(f"{name} must have exactly two values: min max")
    a, b = float(two_vals[0]), float(two_vals[1])
    lo, hi = (a, b) if a <= b else (b, a)
    return lo, hi

def generate_starfield(
    size: int = 1200,
    extent: float = 8.0,
    modes: int = 72,
    mode_types: Optional[List[str]] = None,
    power: float = 6.0,
    percentile: float = 99.7,
    max_stars: int = 8000,
    psf_sigma: float = 1.0,
    gamma: float = 0.8,
    band: Optional[dict] = None,
    seed: Optional[int] = None,
    return_coords: bool = True,
    **_unused,
):
    """
    Generate a synthetic starfield intensity map on a regular grid.

    This routine synthesizes an interference field using randomly oriented plane waves,
    picks bright peaks above a high percentile, stamps a small PSF at each peak to
    approximate point sources and returns the resulting continuous image.  If
    ``return_coords`` is True, the corresponding X and Y coordinate grids (world
    coordinates spanning ``[-extent, extent]``) are also returned.

    Parameters
    ----------
    size : int
        Image resolution (pixels per side).
    extent : float
        Half-width of the world coordinate grid.  Coordinates span ``[-extent, extent]``.
    modes : int
        Number of plane wave modes to superpose when building the interference field.
    power : float
        Exponent applied when rectifying the interference field to accentuate peaks.
    percentile : float
        Percentile threshold used to select bright peaks for star placement.
    max_stars : int
        Maximum number of point sources stamped into the map (subsampled randomly
        if more peaks exceed the percentile threshold).
    psf_sigma : float
        Standard deviation of the Gaussian point spread function used when
        stamping stars on the canvas.
    gamma : float
        Gamma correction applied to the final intensity map.
    band : dict or None
        Optional dictionary specifying a Milky Way-like band with keys
        ``angle`` (degrees), ``width`` and ``strength``.  If provided the
        intensity is multiplied by ``1 + strength * exp(-(coord/width)**2)``
        where ``coord`` is the rotated coordinate along the specified band angle.
    seed : int or None
        Seed for the random number generator.  If None, OS entropy is used.
    return_coords : bool
        If True, return the X and Y coordinate grids along with the intensity map.

    Returns
    -------
    (X, Y, img) or img
        When ``return_coords`` is True a tuple of the coordinate grids and the
        starfield intensity is returned.  Otherwise only the intensity map is
        returned.
    """
    rng = np.random.default_rng(seed)

    # base grid
    x = np.linspace(-extent, extent, size)
    y = np.linspace(-extent, extent, size)
    X, Y = np.meshgrid(x, y)
    # build interference field Z
    # In the starfield synthesizer we ignore ``mode_types`` and always use
    # randomly oriented plane waves to create a dense peak landscape.  The
    # ``modes`` parameter controls how many such waves are superposed.
    Z = np.zeros_like(X)
    for _ in range(modes):
        # random orientation and wavenumber
        kx, ky = rng.uniform(-1.0, 1.0, 2) * rng.uniform(1.0, 10.0)
        phase = rng.uniform(0.0, 2.0 * np.pi)
        Z += np.cos(kx * X + ky * Y + phase)

    # rectify and emphasize peaks
    Zp = np.abs(Z) ** power
    # threshold by percentile
    thresh = np.percentile(Zp, percentile)
    mask = Zp > thresh
    yy, xx = np.where(mask)
    # randomly sample down to max_stars
    if xx.size > max_stars:
        idx = rng.choice(xx.size, size=max_stars, replace=False)
        xx, yy = xx[idx], yy[idx]

    # canvas for stars
    img = np.zeros_like(Zp, dtype=float)
    for px, py in zip(xx, yy):
        img[py, px] = 1.0
    # apply point spread function
    if psf_sigma and psf_sigma > 0.0:
        img = gaussian_filter(img, sigma=psf_sigma)
    # apply optional galactic band
    if band:
        ang = np.deg2rad(float(band.get("angle", 0.0)))
        width = float(band.get("width", 1.0))
        strength = float(band.get("strength", 2.0))
        coords = X * np.cos(ang) + Y * np.sin(ang)
        bandmask = np.exp(- (coords / max(width, 1e-6)) ** 2)
        img *= (1.0 + strength * bandmask)
    # gamma correction
    if gamma and gamma != 1.0:
        img = img ** gamma

    return (X, Y, img) if return_coords else img

def rng_from_seed(seed_str: Optional[str]) -> np.random.RandomState:
    if seed_str is None or str(seed_str).lower() == "random":
        return np.random.RandomState(None)  # OS entropy
    try:
        return np.random.RandomState(int(seed_str))
    except Exception:
        return np.random.RandomState(abs(hash(seed_str)) % (2**32))

def spiral_mode(x: np.ndarray, y: np.ndarray, kr: float, ktheta: float) -> np.ndarray:
    r = np.sqrt(x*x + y*y) + 1e-9
    th = np.arctan2(y, x)
    return np.cos(kr * r + ktheta * th)

def plane_mode(x: np.ndarray, y: np.ndarray, kx: float, ky: float, phase: float) -> np.ndarray:
    return np.cos(kx * x + ky * y + phase)

def radial_mode(x: np.ndarray, y: np.ndarray, kr: float, phase: float) -> np.ndarray:
    r = np.sqrt(x*x + y*y)
    return np.cos(kr * r + phase)

def normalize_field(z: np.ndarray) -> np.ndarray:
    zmin, zmax = np.min(z), np.max(z)
    if np.isclose(zmax, zmin):
        return np.zeros_like(z)
    return (z - zmin) / (zmax - zmin)

# ---------------------------- Core ----------------------------
def generate_field(
    size: int = 1200,
    extent: float = 3.0,
    modes: int = 12,
    mode_types: List[str] = None,
    kx_range=(1.0, 8.0),
    ky_range=(1.0, 8.0),
    kr_range=(1.0, 8.0),
    ktheta_range=(1.0, 8.0),
    amp_range=(0.6, 1.2),
    phase_range=(0.0, 2*np.pi),
    time_t: float = 0.0,
    time_scale: float = 0.3,
    center: Tuple[float, float] = (0.0, 0.0),
    rng: np.random.RandomState = None,
):
    """
    Generate a field on a square grid centered at `center` with half-width `extent`.
    Returns world-coordinate grids X, Y and field Z.
    """
    if mode_types is None or len(mode_types) == 0:
        mode_types = ["plane", "radial", "spiral"]
    if rng is None:
        rng = np.random.RandomState(None)

    # Grid in world coordinates
    cx, cy = center
    lin = np.linspace(-extent, extent, size)
    X_local, Y_local = np.meshgrid(lin, lin)
    X = X_local + cx
    Y = Y_local + cy

    # Build modes
    Z = np.zeros_like(X)
    for _ in range(modes):
        mtype = rng.choice(mode_types)
        amp = rng.uniform(*amp_range)

        if mtype == "plane":
            kx = rng.uniform(*kx_range)
            ky = rng.uniform(*ky_range)
            ph = rng.uniform(*phase_range) + time_scale * time_t
            Z += amp * plane_mode(X, Y, kx, ky, ph)

        elif mtype == "radial":
            kr = rng.uniform(*kr_range)
            ph = rng.uniform(*phase_range) + time_scale * time_t
            Z += amp * radial_mode(X, Y, kr, ph)

        elif mtype == "spiral":
            kr = rng.uniform(*kr_range)
            kth = rng.uniform(*ktheta_range)
            Z += amp * spiral_mode(X, Y, kr + 0.2*time_t*time_scale, kth)

        else:
            kx = rng.uniform(*kx_range)
            ky = rng.uniform(*ky_range)
            ph = rng.uniform(*phase_range) + time_scale * time_t
            Z += amp * plane_mode(X, Y, kx, ky, ph)

    return X, Y, Z

def render_field(
    X, Y, Z,
    out_path: str,
    cmap: str = "inferno",
    contours: bool = True,
    colorbar: bool = False,
    title: str = None,
    dpi: int = 180
):
    fig, ax = plt.subplots(figsize=(8, 8))
    ax.set_aspect("equal")
    ax.axis("off")

    # Normalize for consistent color range
    Zn = normalize_field(Z)
    im = ax.imshow(Zn, extent=(X.min(), X.max(), Y.min(), Y.max()),
                   origin="lower", cmap=cmap, interpolation="bilinear")

    if contours:
        ax.contour(X, Y, Zn, levels=16, colors="white", linewidths=0.25, alpha=0.35)

    if title:
        ax.set_title(title, fontsize=10)

    if colorbar:
        plt.colorbar(im, ax=ax, fraction=0.046, pad=0.04)

    fig.tight_layout(pad=0)
    fig.savefig(out_path, dpi=dpi, bbox_inches="tight", pad_inches=0)
    plt.close(fig)

def animate_field(
    out_path: str,
    frames: int = 240,
    fps: int = 30,
    metadata: dict = None,
    **gen_kwargs
):
    """
    Animate time evolution by calling generate_field(time_t=t, **gen_kwargs).
    Any new kwargs like center, contours, etc., are respected via gen_kwargs.
    """
    # Determine desired colormap and whether contours should be drawn
    cmap = gen_kwargs.get("cmap", "inferno")
    draw_contours = gen_kwargs.get("contours", True)

    # Attempt to use ffmpeg if available; otherwise fall back to cv2 VideoWriter
    ffmpeg_available = shutil.which("ffmpeg") is not None
    # Define a helper to animate using matplotlib's FFMpegWriter
    if ffmpeg_available:
        # Construct writer with metadata if provided
        writer = animation.FFMpegWriter(fps=fps, bitrate=8000, metadata={
            'title': 'Resonance Lab Animation',
            'comment': (json.dumps(metadata)[:2000] if metadata else '')
        })

        fig, ax = plt.subplots(figsize=(8, 8))
        ax.set_aspect("equal")
        ax.axis("off")

        # Initialize first frame
        X0, Y0, Z0 = generate_field(time_t=0.0, **gen_kwargs)
        Zn0 = normalize_field(Z0)
        im = ax.imshow(Zn0, extent=(X0.min(), X0.max(), Y0.min(), Y0.max()),
                       origin="lower", cmap=cmap, interpolation="bilinear")
        ctr = None

        try:
            with writer.saving(fig, out_path, dpi=160):
                for t in range(frames):
                    X, Y, Z = generate_field(time_t=float(t), **gen_kwargs)
                    Zn = normalize_field(Z)
                    im.set_extent((X.min(), X.max(), Y.min(), Y.max()))
                    im.set_data(Zn)

                    # Clear previous contours
                    if ctr is not None:
                        try:
                            for coll in ctr.collections:
                                coll.remove()
                        except Exception:
                            pass
                        ctr = None
                    # Draw new contours if requested
                    if draw_contours:
                        ctr = ax.contour(X, Y, Zn, levels=16, colors="white", linewidths=0.25, alpha=0.35)

                    writer.grab_frame()
        except Exception:
            # If ffmpeg fails mid-run fall back to cv2
            ffmpeg_available = False
        finally:
            plt.close(fig)

    if not ffmpeg_available:
        # Fallback using cv2.VideoWriter
        fig, ax = plt.subplots(figsize=(8, 8))
        ax.set_aspect("equal")
        ax.axis("off")
        # Initialize first frame
        X0, Y0, Z0 = generate_field(time_t=0.0, **gen_kwargs)
        Zn0 = normalize_field(Z0)
        im = ax.imshow(Zn0, extent=(X0.min(), X0.max(), Y0.min(), Y0.max()),
                       origin="lower", cmap=cmap, interpolation="bilinear")
        ctr = None
        # Draw initial contours if requested
        if draw_contours:
            ctr = ax.contour(X0, Y0, Zn0, levels=16, colors="white", linewidths=0.25, alpha=0.35)
        # Render canvas to obtain dimensions
        fig.canvas.draw()
        canvas_width, canvas_height = fig.canvas.get_width_height()
        # cv2 expects (width, height)
        video_writer = cv2.VideoWriter(out_path, cv2.VideoWriter_fourcc(*'mp4v'), fps, (canvas_width, canvas_height))
        # Write first frame
        frame = np.frombuffer(fig.canvas.tostring_rgb(), dtype=np.uint8)
        frame = frame.reshape((canvas_height, canvas_width, 3))
        video_writer.write(cv2.cvtColor(frame, cv2.COLOR_RGB2BGR))
        # Iterate over subsequent frames
        for t in range(1, frames):
            X, Y, Z = generate_field(time_t=float(t), **gen_kwargs)
            Zn = normalize_field(Z)
            im.set_extent((X.min(), X.max(), Y.min(), Y.max()))
            im.set_data(Zn)
            # Remove previous contours
            if ctr is not None:
                try:
                    for coll in ctr.collections:
                        coll.remove()
                except Exception:
                    pass
                ctr = None
            # Draw contours for this frame if requested
            if draw_contours:
                ctr = ax.contour(X, Y, Zn, levels=16, colors="white", linewidths=0.25, alpha=0.35)
            fig.canvas.draw()
            frame = np.frombuffer(fig.canvas.tostring_rgb(), dtype=np.uint8)
            frame = frame.reshape((canvas_height, canvas_width, 3))
            video_writer.write(cv2.cvtColor(frame, cv2.COLOR_RGB2BGR))
        video_writer.release()
        plt.close(fig)

